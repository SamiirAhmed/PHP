<?php


$day = 5; 

switch ($day) {
    case 1:
        echo "Saturday";
        break;
    case 2:
        echo "Sunday";
        break;
    case 3:
        echo "Monday";
        break;
    case 4:
        echo "thuesday";
        break;
    case 5:
        echo "wednesday";
        break;
    case 6:
        echo "thursday";
        break;
    case 7:
        echo "Friday";
        break;
    default:
        echo "Invalid day";
        break;
}
?>
